class Densi {
    private double width;
    private double height;
    private double depth;
    private double mass;
    private double density;

    public Densi(double width, double height, double depth) {
        this.width = width;
        this.height = height;
        this.depth = depth;
    }
    public void setWidth(double width) {
        this.width = width;
    }
    public void setMass(double mass) {
        this.mass = mass;
    }
}